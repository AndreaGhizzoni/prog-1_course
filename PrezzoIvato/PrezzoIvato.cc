#include <iostream>
using namespace std;


//=======================================================================//
// Main
//=======================================================================//
int main () 
{
	float prezzo;
	const float iva = 21.0/100.0;

	cout << "Inserisci il prezzo sul quale calcolare l'iva: " ;
	cin >> prezzo;
	
	prezzo += ( prezzo * iva );
	
	cout << "Il prezzo ivato e': " << prezzo << endl;
	return 0;
}
