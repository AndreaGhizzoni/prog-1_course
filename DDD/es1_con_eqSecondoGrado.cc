#include <iostream>
#include <cmath>
using namespace std;

//compilare con -g e lanciare ddd <eseguibile>

//=======================================================================//
// Main
//=======================================================================//
int main () 
{
	double a, b, c;
	cin >> a, b, c;
	
	double delta = b*b - 4*a*c;

	if( delta > 0 )
	{
		cout << "x1: " << (-b+sqrt(delta)) / (2*a) << endl;
		cout << "x2: " << (-b-sqrt(delta)) / (2*a) << endl;
	}
	return 0;
}
